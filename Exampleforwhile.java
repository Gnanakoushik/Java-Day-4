import java.util.*;
public class Exampleforwhile{
    public static void main(String args[])
    {
        Scanner sc=new Scanner(System.in);
        int n=sc.nextInt();
        System.out.println(n++);
        int a=sc.nextInt();
        System.out.println(++a);
        int b=sc.nextInt();
        System.out.println(b--);
        int c=sc.nextInt();
        System.out.println(--c);
    }
}